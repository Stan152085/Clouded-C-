#pragma once
#include "Texel.h"

namespace resources
{
  class Texture;

  class Material
  {
    ///////////////////////////////////////////////////////////////////////////
    // Base color
    ///////////////////////////////////////////////////////////////////////////
    Texel base_color_factor_;
    Texture* base_color_texture_;

    ///////////////////////////////////////////////////////////////////////////
    // roughness and metalicness
    ///////////////////////////////////////////////////////////////////////////
    float rough_factor_;
    float metalic_factor_; 
    // metallic in the blue channel, roughness in green
    Texture* metallic_rough_texture_;

    ///////////////////////////////////////////////////////////////////////////
    // normal map
    ///////////////////////////////////////////////////////////////////////////
    float normal_scale_;
    Texture* normal_texture_;

    ///////////////////////////////////////////////////////////////////////////
    // occlusion map
    ///////////////////////////////////////////////////////////////////////////
    float occlusion_strength_;
    // the value is in the red channel
    Texture* occlusion_texture_;

    ///////////////////////////////////////////////////////////////////////////
    // emission map
    ///////////////////////////////////////////////////////////////////////////
    float emissive_factor_;
    Texture* emissive_texture;
  };
}
